    private boolean validarCampos(){
        boolean error= false;
        if(txtNombre.getText().trim().isEmpty()){
            showError(txtNombre, "Por favor digite el nombre del nuevo usuario.");
            error= true;
        }
        if(txtUserName.getText().isEmpty() || txtUserName.getText().trim().split(" ").length == 2){
            showError(txtUserName, "Por favor digite un nombre de usuario sin espacios.");
            error= true;
        }
        if(txtClave.getText().trim().isEmpty() || txtClave.getText().trim().split(" ").length == 2){
            showError(txtClave, "Por favor digite una clave sin espacios.");
            error=true;
        }
        return error;
    }

