    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if(validarCampos()){
            JOptionPane.showMessageDialog(this, "Por favor verifique que los campos esten correctos.", "adBiblioteca", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String nombreBibliotecario= txtNombre.getText().trim();
        String userName= txtUserName.getText().trim();
        char [] pass= txtClave.getText().trim().toCharArray();
        Log suso= Log.existe(userName);
        if(suso == null){
            Log nuevoUsuario= new Log(nombreBibliotecario, userName, pass);
            if(nuevoUsuario.insertarRol()){
                JOptionPane.showMessageDialog(this, "Exito... Se registro un nuevo usuario de la aplicaciÃ³n.", "adBiblioteca", JOptionPane.INFORMATION_MESSAGE);
                tabla.setModel(Log.getSusos());
            }
        }
       else{
            JOptionPane.showMessageDialog(this, "El userName ya existe por favor verifique que el usuario no haya sido creado\n"
                                                +"o utilice otro userName para crear la cuenta",
                                          "adBiblioteca", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_btnAceptarActionPerformed

